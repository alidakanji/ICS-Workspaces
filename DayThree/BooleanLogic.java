package DayThree;

public class BooleanLogic {
    public static void main(String[] args) {
        boolean is_sunny ;
        is_sunny=true ;
        boolean is_weekend ;
        is_weekend=false ;
        System.out.print (is_sunny) ;
        System.out.println (", it is sunny.") ;
        System.out.print (is_weekend);
        System.out.print(", it is not the weekend");
}
}