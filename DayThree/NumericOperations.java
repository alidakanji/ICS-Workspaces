package DayThree;

public class NumericOperations {
    public static void main(String[] args) {
        int num1=2 ;
        int num2=4 ;
        System.out.println(num1+num2) ;
        System.out.println(num1-num2) ;
        System.out.println(num1*num2) ;
        System.out.println(num1/num2) ;
}
}